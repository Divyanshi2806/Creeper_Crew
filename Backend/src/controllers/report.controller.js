const Anthropic = require('@anthropic-ai/sdk')
const nodemailer = require('nodemailer')
const { PrismaClient } = require('@prisma/client')
const prisma = new PrismaClient()

const anthropic = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY })

const generateReport = async (req, res) => {
  const { studentId, teacherNotes = '' } = req.body

  if (!studentId) return res.status(400).json({ error: 'studentId is required.' })

    const student = await prisma.student.findUnique({
    where: { id: parseInt(studentId) },
    include: {
      class: true,
      attendance: { orderBy: { date: 'desc' }, take: 30 },
      grades: { orderBy: { examDate: 'desc' } }
    }
  })

  if (!student) return res.status(404).json({ error: 'Student not found.' })
    const total = student.attendance.length
  const present = student.attendance.filter(a => a.status === 'present').length
  const late = student.attendance.filter(a => a.status === 'late').length
  const attendancePercent = total > 0
    ? Math.round(((present + late * 0.5) / total) * 100)
    : 0
    const gradesBySubject = student.grades.reduce((acc, g) => {
    if (!acc[g.subject]) acc[g.subject] = { total: 0, max: 0, count: 0 }
    acc[g.subject].total += g.marks
    acc[g.subject].max += g.maxMarks
    acc[g.subject].count += 1
    return acc
  }, {})

  const subjectGradesText = Object.entries(gradesBySubject)
    .map(([subject, data]) => {
      const pct = Math.round((data.total / data.max) * 100)
      return `${subject}: ${pct}% (${pct >= 40 ? 'Pass' : 'Fail'})`
    })
    .join(', ')

    const prompt = `You are a professional school report writer. Generate a warm, formal parent progress report for the following student. Keep it under 200 words. Be encouraging but honest.

Student: ${student.name}
Class: ${student.class.name}
Attendance: ${attendancePercent}% this month
Subjects and grades: ${subjectGradesText}
Teacher notes: ${teacherNotes || 'Student is making steady progress.'}

Write the report now:`
const message = await anthropic.messages.create({
    model: 'claude-opus-4-5',
    max_tokens: 400,
    messages: [{ role: 'user', content: prompt }]
  })

  const reportText = message.content[0].text

  res.json({
    report: reportText,
    student: {
      id: student.id,
      name: student.name,
      parentEmail: student.parentEmail,
      class: student.class.name,
      attendancePercent,
      subjectGrades: subjectGradesText,
    }
  })
}
const sendReport = async (req, res) => {
  const { studentId, reportText } = req.body

  if (!studentId || !reportText) {
    return res.status(400).json({ error: 'studentId and reportText are required.' })
  }

  const student = await prisma.student.findUnique({
    where: { id: parseInt(studentId) },
    include: { class: true }
  })

  if (!student) return res.status(404).json({ error: 'Student not found.' })

  // Set up Nodemailer with Gmail
  const transporter = nodemailer.createTransport({
    service: 'gmail',
    auth: {
      user: process.env.EMAIL_USER,
      pass: process.env.EMAIL_PASS, // Gmail App Password
    }
  })

  await transporter.sendMail({
    from: `"EduEase School" <${process.env.EMAIL_USER}>`,
    to: student.parentEmail,
    subject: `Progress Report — ${student.name} (${student.class.name})`,

    text: reportText,

    html: `
      <div style="font-family: Arial, sans-serif; max-width: 600px; margin: 0 auto; padding: 24px;">
        <div style="background: #7C3AED; padding: 20px; border-radius: 8px 8px 0 0;">
          <h1 style="color: white; margin: 0; font-size: 22px;">EduEase</h1>
          <p style="color: #EDE9FE; margin: 4px 0 0;">Student Progress Report</p>
        </div>
        <div style="background: #ffffff; padding: 24px; border: 1px solid #E5E7EB; border-radius: 0 0 8px 8px;">
          <h2 style="color: #1A1A2E;">${student.name} — ${student.class.name}</h2>
          <div style="white-space: pre-wrap; color: #374151; line-height: 1.7;">${reportText}</div>
          <hr style="margin: 24px 0; border-color: #E5E7EB;" />
          <p style="color: #6B7280; font-size: 13px;">This report was generated by EduEase. Please contact the school for any queries.</p>
        </div>
      </div>
    `
  })

  res.json({ message: `Report sent to ${student.parentEmail} successfully.` })
}

module.exports = { generateReport, sendReport }